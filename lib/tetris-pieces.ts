// Color scheme using danger/rose colors
export const COLORS: Record<TetrominoType, string> = {
  I: "#f43f5e", // rose-500 (primary danger color)
  O: "#fb7185", // rose-400
  T: "#fda4af", // rose-300
  S: "#fecdd3", // rose-200
  Z: "#ffe4e6", // rose-100
  J: "#ff1744", // deeper red
  L: "#e91e63", // pink
};

// Get the shape matrix for a specific piece type and rotation
export function getPieceShape(
  type: TetrominoType,
  rotation: 0 | 1 | 2 | 3,
): number[][] {
  return PIECES[type][rotation];
}

// Get a random tetromino type
export function getRandomPiece(): TetrominoType {
  const types: TetrominoType[] = ["I", "O", "T", "S", "Z", "J", "L"];

  return types[Math.floor(Math.random() * types.length)];
}

// Get the color for a specific piece type
export function getPieceColor(type: TetrominoType): string {
  return COLORS[type];
}
